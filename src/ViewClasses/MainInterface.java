    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        tskListCtrl.getAllTaskListFromFile();   //load data again from file due to handle the extreme situation(sort button selected situation)  
        try {           //this try for handle invalid number format
            int idToRemove = Integer.parseInt(JOptionPane.showInputDialog("Enter Task ID"));
            Task removedTask = tskListCtrl.getTskList().deleteTask(idToRemove);
            if (removedTask != null) {
                tskListCtrl.updateFile(tskListCtrl.getTskList()); //overwrite file with new task list after remove task
                allTaskBtn.setSelected(true);    //handle all task btn
                sortBtnGrp.clearSelection();    //clear sort btn
                loadtable();    //update table
                displaySummary();   //update summary view
                JOptionPane.showMessageDialog(null, "Task removed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Task not found", "Error", JOptionPane.ERROR_MESSAGE);
            } 
        } catch (NumberFormatException e) {
            
        }  
        
    }//GEN-LAST:event_removeBtnActionPerformed

    
    
    
    
    
    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        tskListCtrl.getAllTaskListFromFile(); //load data from file handle to if sorting select before the update
        try {        //try catch handle invalid number format
            int inputId = Integer.parseInt(idTxt.getText());        //conver string to int
            if (tskListCtrl.getTskList().findTask(inputId) != null) {       //check ther is a task in that ID
                if (completeCheckBox.isSelected()) {
                    tskListCtrl.getTskList().markCompleteTask(inputId);
                    
                    //overwrite file
                    tskListCtrl.updateFile(tskListCtrl.getTskList());     
                    loadtable();    //update table
                    displaySummary();   //update summary view
                    allTaskBtn.setSelected(true);    //handle all task btn
                    sortBtnGrp.clearSelection();    //clear sort btn
                    JOptionPane.showMessageDialog(null, "Mark Task As Completed Task!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    idTxt.setText("");      //clear vaules
                    completeCheckBox.setSelected(false);    //clear vaules
                }else{
                    JOptionPane.showMessageDialog(null, "Mark Task as completed", "Question", JOptionPane.QUESTION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Task not found", "Error", JOptionPane.ERROR_MESSAGE);
                idTxt.setText("");      //clear vaules
                completeCheckBox.setSelected(false);    //clear vaules
            }           
        } catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(null, "Enter Task Id", "Warning", JOptionPane.WARNING_MESSAGE);
        }  
        
    }//GEN-LAST:event_saveBtnActionPerformed

    
    
    
