    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        //load data from file. because we need to remove affect for taskList from using sort buttons
        tskListCtrl.getAllTaskListFromFile();   

        if(!"".equals(titleTxt.getText()) && dueDate.getDate() != null){
            
            //convert util date to local Date
            Date utilDate = dueDate.getDate();
            Instant instant = utilDate.toInstant();
            ZoneId zoneId = ZoneId.systemDefault();
            ZonedDateTime zonedDateTime = instant.atZone(zoneId);
            LocalDate localDate = zonedDateTime.toLocalDate();
            
            //generate ID
            tempId = tempId + 1;
            
            //create task object with user values
            Task t = new Task(tempId, titleTxt.getText(), descriptionTxt.getText(), localDate);
            
            //add task to my task List
            tskListCtrl.getTskList().addTask(t);

            //update file
            tskListCtrl.updateFile(tskListCtrl.getTskList());
            
            loadtable();    //refresh table
            
            displaySummary();   //update summary view
            allTaskBtn.setSelected(true);   //select all task radio button
            sortBtnGrp.clearSelection();      //clear sorting selection
            JOptionPane.showMessageDialog(null, "Task Added Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            //clear values
            titleTxt.setText("");
            descriptionTxt.setText("");
            dueDate.setDate(null);    
        }else{
            JOptionPane.showMessageDialog(null, "Please Fill details");
        }   
    }//GEN-LAST:event_AddBtnActionPerformed

